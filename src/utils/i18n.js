function getLang() {
  // In a browser extension content script, navigator.language is available
  const lang = (typeof navigator !== 'undefined' && navigator.language) 
    ? navigator.language.substring(0, 2).toLowerCase() 
    : 'de';
  return translations[lang] ? lang : 'de';
}

function t(key, replacements = {}) {
  const lang = getLang();
  let text = translations[lang][key] || translations['de'][key] || key;
  for (const [k, v] of Object.entries(replacements)) {
    text = text.replace(`$${k}$`, v);
  }
  return text;
}

if (typeof module !== 'undefined' && module.exports) {
  module.exports = { t, getLang, translations };
} else {
  window.swissAiI18n = { t, getLang };
}